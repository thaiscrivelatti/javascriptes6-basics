/**
 * Módulo (%)
 * Operador binario. Retorna o inteiro restante da divisão dos dois operandos.
 */
console.log("Resto de 12/5: ", 12 % 5); // retorna 2

/**
 * Operador unario 
 * Pode vir antes do operando ou depois
 * Incremento ++
 * Decremento --
 * 
 * var x;
 * x++;
 * x--;
 */

// Negação e adição
console.log(-3);
console.log(+"3"); // retorna 3
console.log(+true) // retorna 1
console.log(+false) //retorna 0
console.log(-true) // retorna -1

