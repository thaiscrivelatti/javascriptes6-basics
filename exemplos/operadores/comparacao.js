// Igual
console.log('3'==3);

// NÃ£o igual
console.log('3'!=3);

// Estritamente igual
// Operandos devem ser iguais e ter o mesmo tipo
console.log(3===3); // true
console.log('3'===3); // false
 
// Estritamente nao igual
// Operandos diferentes e tipos diferentes
console.log(3!==3); // false
console.log('3'!==3); // true

// Maior que 
console.log(10>2);

// Maior que ou igual 
console.log(10>=10); // true

// Menor que 
console.log(5<15);

// Menor que ou igual 
console.log(5<=15); // true
